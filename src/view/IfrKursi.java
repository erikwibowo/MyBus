    private void tbDataKursiMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbDataKursiMouseClicked
        if(evt.getClickCount() == 2){
           int row = tbDataKursi.getSelectedRow();
           kursi.id_kursi = tbDataKursi.getValueAt(row, 0).toString();
           kursi.vno_kursi= tbDataKursi.getValueAt(row, 1).toString();
           
           
           txtIdKursi.setText(kursi.id_kursi);
           txtNoKursi.setText(kursi.vno_kursi);
           
           enableInput();
           txtIdKursi.setEditable(false);
           btnHapus.setEnabled(true);
           lblSimpan.setText("Ubah");
           txtNoKursi.requestFocus(true);
           lblTambah.setText("Batal");
           btnTambah.setBackground(new java.awt.Color(240,50,95));
           lblTambah.setIcon(new javax.swing.ImageIcon(getClass().
                   getResource("/source/ic_cancel_white_18dp.png")));
       }
    }//GEN-LAST:event_tbDataKursiMouseClicked

