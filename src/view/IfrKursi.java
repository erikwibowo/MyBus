    private void setTabelKursi(){
        String[] kolom1 = {"ID. Kursi", "Nomor Kursi"};
        kursi.tblkursi = new DefaultTableModel(null, kolom1){
            Class[] types = new Class[]{
                java.lang.String.class,
                java.lang.String.class
            };
            
            public Class getColumnClass(int columnIndex){
            return types [columnIndex];
            }
            
            
            public boolean isCellEditable(int row, int col){
            int cola = kursi.tblkursi.getColumnCount();
            return (col < cola) ? false : true;
        }
       };
        tbDataKursi.setModel(kursi.tblkursi);
        tbDataKursi.getColumnModel().getColumn(0).setPreferredWidth(75);
        tbDataKursi.getColumnModel().getColumn(1).setPreferredWidth(100);
    }
    
    private void clearTabelKursi(){
        int row  = kursi.tblkursi.getRowCount();
        for(int i=0; i<row; i++){
            kursi.tblkursi.removeRow(0);//menghapus record/baris pd tabel kursi
        }
    }
    
    private void showDataKursi(){
        tbDataKursi.setModel(kursi.tblkursi);
        int row = kursi.tblkursi.getRowCount();
        for(int i=0; i<row; i++){
            kursi.tblkursi.removeRow(0);
        }
        kursi.select();
        for(Object obj: kursi.list){
            kursi.tblkursi.addRow((Object[]) obj);
        }
        lblRecord.setText("Record : "+ tbDataKursi.getRowCount());
    }
    
    private void aksiSimpan(){
        if(txtIdKursi.getText().equals("") || txtNoKursi.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Mohon lengkapi data", 
                    "Informasi", JOptionPane.INFORMATION_MESSAGE);
        }else{
            kursi.isUpdate = !lblSimpan.getText().equals("Simpan");
            kursi.id_kursi = txtIdKursi.getText();
            kursi.vno_kursi = txtNoKursi.getText();
            kursi.insert_update();
            JOptionPane.showMessageDialog(this, "Data berhasil disimpan",
                    "Informasi", JOptionPane.INFORMATION_MESSAGE);
            clearInput(); 
            disableInput(); 
            showDataKursi();
        }
    }
    
    private void aksiHapus(){
        if(txtIdKursi.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Informasi",
                    "Anda belum memilih data yang akan dihapus", JOptionPane.INFORMATION_MESSAGE);
        }else {
            int jawab = JOptionPane.showConfirmDialog(this, "Apakah anda akan menghapus data ini? Kode : "+kursi.id_kursi,
                    "Konfirmasi", JOptionPane.YES_NO_OPTION);
            if(jawab == JOptionPane.YES_OPTION){
                kursi.delete(kursi.id_kursi);
                JOptionPane.showMessageDialog(this, "Data Berhasil dihapus",
                        "Informasi", JOptionPane.INFORMATION_MESSAGE);
                clearInput(); 
                disableInput(); 
                showDataKursi();
            }
            
        }
    }
    
    private void formTengah(){
        Dimension screensize = Toolkit.getDefaultToolkit().getScreenSize();
        Dimension framesize = this.getSize();
        if(framesize.height < screensize.height){
            framesize.height = screensize.height;
        }
        if(framesize.width > screensize.width){
            framesize.width = screensize.width;
        }
        this.setLocation((screensize.width - framesize.width)/2, 
                (screensize.height - framesize.height)/2);
    }

